package chess.entity;

public class Player
{
    private String mNick;
    private String mColor;

    public String getNick()
    {
        return mNick;
    }

    public void setNick(String mNick)
    {
        this.mNick = mNick;
    }

    public String getColor()
    {
        return mColor;
    }

    public void setColor(String mColor)
    {
        this.mColor = mColor;
    }
}
